    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String keluhan = jTextArea1.getText().trim(); // Get the complaint text
        if (keluhan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan isi semua data terlebih dahulu.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (selectedDoctorId <= 0) { // Pastikan dokter telah dipilih
            JOptionPane.showMessageDialog(this, "Silakan pilih dokter.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (jTextField1.getText().trim().isEmpty()
                || jTextField3.getText().trim().isEmpty()
                || jTextField4.getText().trim().isEmpty()
                || jTextField5.getText().trim().isEmpty()
                || jTextField6.getText().trim().isEmpty()
                || jTextField8.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan isi semua data pasien.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (idUser < 0) {
            JOptionPane.showMessageDialog(this, "User  belum menambahkan informasi data diri, silahkan tambahkan data pada label informasi.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String nama = getNameById(idUser);
        if (nama == null || nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama pengguna tidak ditemukan.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            String sql = "INSERT INTO detail (keluhan, nama, umur, jnsklmin, alamat, ttl, alergi, id_dokter, id_user, tanggal) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = Koneksi.getConnection().prepareStatement(sql);

            ps.setString(1, keluhan);
            ps.setString(2, jTextField1.getText().trim());
            ps.setString(3, jTextField3.getText().trim());
            ps.setString(4, jTextField4.getText().trim());
            ps.setString(5, jTextField5.getText().trim());
            ps.setString(6, jTextField6.getText().trim());
            ps.setString(7, jTextField8.getText().trim());
            ps.setInt(8, selectedDoctorId);
            ps.setInt(9, idUser);
            ps.setTimestamp(10, new java.sql.Timestamp(System.currentTimeMillis())); // Tanggal dan waktu saat ini

            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin sudah menginput data diri dan keluhan dengan detail dan benar?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
                if (confirm != JOptionPane.YES_OPTION) {
                    return;
                } else {
                    JOptionPane.showMessageDialog(this, "Data Yang Anda Input berhasil disimpan.", "Information", JOptionPane.INFORMATION_MESSAGE);
                    jTextArea1.setText("");
                    jInternalFrame1.setVisible(true);
                    loadRiwayatKonsultasi();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saat menyimpan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

