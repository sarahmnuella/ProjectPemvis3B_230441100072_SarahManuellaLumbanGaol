 private void loadRiwayatKonsultasi() {
        try {
            String sql = "SELECT d.id_detail, d.nama, d.umur, d.jnsklmin, d.alamat, d.ttl, d.alergi, doc.nama AS dokter, d.keluhan, d.saran, d.tanggal "
                    + "FROM detail d "
                    + "LEFT JOIN dokter doc ON d.id_dokter = doc.id_dokter "
                    + "WHERE d.id_user = ?";
            PreparedStatement ps = Koneksi.getConnection().prepareStatement(sql);
            ps.setInt(1, idUser);
            ResultSet rs = ps.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id_detail"),
                    rs.getString("nama"),
                    rs.getString("umur"),
                    rs.getString("jnsklmin"),
                    rs.getString("alamat"),
                    rs.getString("ttl"),
                    rs.getString("alergi"),
                    rs.getString("dokter"),
                    rs.getString("keluhan"),
                    rs.getString("saran"),
                    rs.getTimestamp("tanggal") // Ambil tanggal dari hasil query
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saat memuat riwayat konsultasi.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

