    private boolean isUsernameTaken(String username) {
        try {
            String sql = "SELECT * FROM login WHERE email = ?";
            PreparedStatement ps = Koneksi.getConnection().prepareStatement(sql);
            ps.setString(1, username);
            ResultSet rs = ps.executeQuery();
            return rs.next(); // Jika ada hasil, berarti username sudah ada
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String emailReg = jTextField2.getText();
        String passwordReg = new String(jPasswordField2.getPassword());

        if (emailReg.isEmpty() || passwordReg.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (isUsernameTaken(emailReg)) {
            JOptionPane.showMessageDialog(this, "Username sudah digunakan oleh akun lain, Gunakan username lain!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            String sql = "INSERT INTO login (email, password) VALUES (?, ?)";
            PreparedStatement ps = Koneksi.getConnection().prepareStatement(sql);
            ps.setString(1, emailReg);
            ps.setString(2, passwordReg);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Registrasi berhasil! Silakan login.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            jInternalFrame1.dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Email sudah terdaftar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

